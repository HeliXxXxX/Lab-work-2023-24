A = 4534
B = 2345

#the sum
print( f'The sum = {A + B}' )

#the difference 
print( f'The difference = {A - B}')


#the product
print(f'The product = {A*B}')

#the avarage
print(f'The avarage = {(A+B)/2}')

#the distance
if A > B:
    print( f'The distance = {A - B}')
else:
    print(f'The distance = {B - A}')

